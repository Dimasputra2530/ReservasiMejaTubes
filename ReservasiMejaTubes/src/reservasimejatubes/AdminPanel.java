    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String nama = JOptionPane.showInputDialog(this, "Masukkan Nama Makanan:");
        if (nama == null || nama.trim().isEmpty()) {
            return;
        }

        String hargaInput = JOptionPane.showInputDialog(this, "Masukkan Harga:");
        if (hargaInput == null || hargaInput.trim().isEmpty()) {
            return;
        }

        String stokInput = JOptionPane.showInputDialog(this, "Masukkan Stok:");
        if (stokInput == null || stokInput.trim().isEmpty()) {
            return;
        }

        try {
            double harga = Double.parseDouble(hargaInput);
            int stok = Integer.parseInt(stokInput);

            // Simpan ke database
            try (Connection conn = DatabaseConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(
                    "INSERT INTO namamakanan (nama, harga, stock) VALUES (?, ?, ?)")) {
                stmt.setString(1, nama);
                stmt.setDouble(2, harga);
                stmt.setInt(3, stok);
                stmt.executeUpdate();
            }

            // Refresh tabel
            loadData();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Harga atau stok tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data!", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

