    private void loadData() {
        DefaultTableModel tableModel = (DefaultTableModel) tableMenu.getModel();
        tableModel.setRowCount(0); // Bersihkan tabel

        // Ambil data dari database
        try (Connection conn = DatabaseConnection.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery("SELECT * FROM namamakanan")) {

            while (rs.next()) {
                tableModel.addRow(new Object[]{
                    rs.getString("nama"),
                    rs.getDouble("harga"),
                    rs.getInt("stock")
                });
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memuat data!", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
